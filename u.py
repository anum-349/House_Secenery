def drawtree(p):
    if p<10:
        return
    else:
        t.pensize(3)
        t.forward(p)
        t.color("orange")
        t.circle(2)
        t.color("green")
        t.left(30)
        drawtree(3*p/4)
        t.right(60)
        drawtree(3*p/4)
        t.left(30)
        t.backward(p)

