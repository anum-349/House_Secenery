def drawsky():
    t.penup()
    t.goto(-900, -170)
    t.pendown()
    t.color("deepskyblue")
    t.begin_fill()
    for i in range(2):
        t.forward(1800)
        t.left(90)
        t.forward(1800)
        t.left(90)
    t.end_fill()
    t.home()

